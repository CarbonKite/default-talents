#!/usr/bin/env python3
"""Claude Code Remote Worker for OneManCompany.

Launches a Claude Code CLI session as a remote employee.
Reads connection config (employee_id, company_url) from the connection.json
generated by HR during hiring. No manual ID or URL needed.

Usage:
    # Provide the employee directory path (contains connection.json from hiring)
    python run_worker.py /path/to/company/human_resource/employees/00010

    # Optional flags
    python run_worker.py /path/to/employees/00010 --max-turns 50 --work-dir /my/project
"""

from __future__ import annotations

import argparse
import asyncio
import json
import subprocess
import sys
import tempfile
from pathlib import Path

# Allow running from anywhere — add project root to path
_PROJECT_ROOT = Path(__file__).resolve().parents[4]
sys.path.insert(0, str(_PROJECT_ROOT))

from onemancompany.talent_market.remote_protocol import TaskAssignment, TaskResult
from onemancompany.talent_market.remote_worker_base import RemoteWorkerBase


def load_connection(employee_dir: Path) -> dict:
    """Load connection.json from the employee directory (written by HR on hire)."""
    conn_file = employee_dir / "connection.json"
    if not conn_file.exists():
        print(f"[ClaudeCodeWorker] ERROR: {conn_file} not found.")
        print("  This file is generated by HR when hiring a remote employee.")
        print("  Make sure the CEO has hired a 'Claude Code Agent' first.")
        sys.exit(1)
    return json.loads(conn_file.read_text(encoding="utf-8"))


class ClaudeCodeWorker(RemoteWorkerBase):
    """Remote worker that delegates tasks to a `claude` CLI subprocess."""

    def __init__(
        self,
        company_url: str,
        employee_id: str,
        *,
        work_dir: str | None = None,
        max_turns: int = 30,
        **kwargs,
    ) -> None:
        super().__init__(
            company_url=company_url,
            employee_id=employee_id,
            capabilities=["coding", "code_review", "debugging", "full_stack"],
            **kwargs,
        )
        self.work_dir = Path(work_dir) if work_dir else Path.cwd()
        self.max_turns = max_turns

    def setup_tools(self) -> list:
        """No LangChain tools — Claude Code CLI is the tool."""
        try:
            result = subprocess.run(
                ["claude", "--version"],
                capture_output=True,
                text=True,
                timeout=10,
            )
            print(f"[ClaudeCodeWorker] Claude CLI: {result.stdout.strip()}")
        except FileNotFoundError:
            print("[ClaudeCodeWorker] ERROR: 'claude' CLI not found in PATH")
            print("  Install: npm install -g @anthropic-ai/claude-code")
            sys.exit(1)
        return []

    async def process_task(self, task: TaskAssignment) -> TaskResult:
        """Run a task through Claude Code CLI and return the result."""
        print(f"\n{'='*60}")
        print(f"[ClaudeCodeWorker] Received task: {task.task_id}")
        print(f"  Project: {task.project_id}")
        print(f"  Description: {task.task_description[:200]}")
        print(f"{'='*60}\n")

        prompt = self._build_prompt(task)

        cwd = self.work_dir
        if task.project_dir and Path(task.project_dir).exists():
            cwd = Path(task.project_dir)

        try:
            output = await self._run_claude(prompt, cwd=cwd)
            status = "completed"
            print(f"\n[ClaudeCodeWorker] Task {task.task_id} completed.")
        except asyncio.TimeoutError:
            output = "Task timed out after maximum allowed duration."
            status = "failed"
            print(f"\n[ClaudeCodeWorker] Task {task.task_id} timed out.")
        except Exception as exc:
            output = f"Error executing task: {exc}"
            status = "failed"
            print(f"\n[ClaudeCodeWorker] Task {task.task_id} failed: {exc}")

        return TaskResult(
            task_id=task.task_id,
            employee_id=self.employee_id,
            status=status,
            output=output[:5000],
            artifacts=[],
        )

    def _build_prompt(self, task: TaskAssignment) -> str:
        """Build a prompt string for Claude Code from the task assignment."""
        parts = [
            f"You are employee {self.employee_id} at OneManCompany.",
            f"Task ID: {task.task_id}",
            f"Project: {task.project_id}",
            "",
            "## Task Description",
            task.task_description,
        ]

        ctx = task.context
        if ctx.get("guidance"):
            parts.extend(["", "## Guidance", ctx["guidance"]])
        if ctx.get("skills"):
            parts.extend(["", f"## Required Skills: {', '.join(ctx['skills'])}"])

        parts.extend([
            "",
            "## Instructions",
            "Complete this task thoroughly. Provide a clear summary of what you did.",
        ])

        return "\n".join(parts)

    async def _run_claude(self, prompt: str, cwd: Path) -> str:
        """Execute `claude` CLI in non-interactive mode and return output."""
        with tempfile.NamedTemporaryFile(
            mode="w", suffix=".md", delete=False, prefix="omc_task_"
        ) as f:
            f.write(prompt)
            prompt_file = f.name

        cmd = [
            "claude",
            "--print",
            "--max-turns", str(self.max_turns),
            prompt,
        ]

        print(f"[ClaudeCodeWorker] Running: claude --print --max-turns {self.max_turns}")
        print(f"[ClaudeCodeWorker] Working dir: {cwd}")

        proc = await asyncio.create_subprocess_exec(
            *cmd,
            cwd=str(cwd),
            stdout=asyncio.subprocess.PIPE,
            stderr=asyncio.subprocess.PIPE,
        )

        stdout, stderr = await asyncio.wait_for(
            proc.communicate(),
            timeout=600,
        )

        Path(prompt_file).unlink(missing_ok=True)

        output = stdout.decode("utf-8", errors="replace")
        if proc.returncode != 0:
            err = stderr.decode("utf-8", errors="replace")
            output += f"\n\n[STDERR]\n{err}" if err else ""

        return output


def main():
    parser = argparse.ArgumentParser(
        description="OneManCompany Claude Code Remote Worker"
    )
    parser.add_argument(
        "employee_dir",
        help="Path to the employee directory (e.g. company/human_resource/employees/00010)",
    )
    parser.add_argument(
        "--work-dir",
        default=None,
        help="Working directory for Claude Code (default: current dir)",
    )
    parser.add_argument(
        "--max-turns",
        type=int,
        default=30,
        help="Max agentic turns per task (default: 30)",
    )
    parser.add_argument(
        "--poll-interval",
        type=float,
        default=5.0,
        help="Seconds between task polls (default: 5.0)",
    )
    args = parser.parse_args()

    emp_dir = Path(args.employee_dir)
    conn = load_connection(emp_dir)

    employee_id = conn["employee_id"]
    company_url = conn["company_url"]

    worker = ClaudeCodeWorker(
        company_url=company_url,
        employee_id=employee_id,
        work_dir=args.work_dir,
        max_turns=args.max_turns,
        poll_interval=args.poll_interval,
    )

    print(f"[ClaudeCodeWorker] Starting remote worker")
    print(f"  Employee ID : {employee_id}")
    print(f"  Company URL : {company_url}")
    print(f"  Talent      : {conn.get('talent_id', 'unknown')}")
    print(f"  Work Dir    : {args.work_dir or 'current directory'}")
    print(f"  Max Turns   : {args.max_turns}")
    print(f"  Poll Interval: {args.poll_interval}s")
    print()

    try:
        asyncio.run(worker.start())
    except KeyboardInterrupt:
        print("\n[ClaudeCodeWorker] Shutting down...")
        worker.stop()


if __name__ == "__main__":
    main()
